import express from 'express';
import bcrypt from 'bcrypt';
const eRouter = express.Router()
import validator from 'express-validator';
import fs from 'fs'
const loadJSON = (path) => JSON.parse(fs.readFileSync(new URL(path, import.meta.url)));
const marketplace = loadJSON('../../marketplace.json')

export function genRealUser(userData) {
    return {
        id: userData.user_id,
        username: userData.username,
        timestamp: new Date(userData.createdIn),
        points: userData.points,
        wins: userData.wins,
        rounds: userData.gamesPlayed,
        shape: userData.shape
    }
}

export function router(db) {
    eRouter.post("/@me/username", validator.body('username').notEmpty().isString(), (req, res) => {
        if (!req.user) return res.sendStatus(401);
        const userData = db.prepare('SELECT * FROM users WHERE user_id = ?').get(req.user.user_id);
        if (!userData) return res.sendStatus(401);
        const result = validator.validationResult(req);
        if (!result.isEmpty()) return res.status(400).json({ errors: result.array() })
        const { username } = req.body;
        if (username.length < 3) return res.status(406).send("Please enter in a username that is longer than 2 characters!");
        if (username.length > 10) return res.status(406).send("Please enter in a username that is less than than 10 characters!");
        if (!/^[a-zA-Z0-9_]+$/.test(username)) return res.status(406).send("Usernames cannot contain any special characters! They can only contain letters, numbers, and an underscore.");
        const userExists = db.prepare('SELECT count(*) FROM users WHERE username = ?').pluck().get(username);
        if (userExists) return res.status(409).send('Someone with that username already exists!');
        db.prepare('UPDATE users SET username = ? WHERE user_id = ?').run(username, req.user.user_id);
        return res.sendStatus(200);
    })
    eRouter.post("/@me/password", validator.body('password').notEmpty().isString(), (req, res) => {
        if (!req.user) return res.sendStatus(401);
        const userData = db.prepare('SELECT * FROM users WHERE user_id = ?').get(req.user.user_id);
        if (!userData) return res.sendStatus(401);
        const result = validator.validationResult(req);
        if (!result.isEmpty()) return res.status(400).json({ errors: result.array() })
        const { password } = req.body;
        if (password.length < 8) return res.status(406).send("Please enter in a password that is longer than 8 characters!");
        if (password.length > 1024) return res.status(406).send("calm down");
        return bcrypt.hash(`${userData.salt}.${password}`, 10, function(err, hash) {
            if (err) return res.status(500).send("An error occured while trying to hash the password.");
            db.prepare('UPDATE users SET password = ? WHERE user_id = ?').run(hash, req.user.user_id);
            res.sendStatus(200);
        })
    })
    eRouter.route("/@me").get((req, res) => {
        if (!req.user) return res.sendStatus(401);
        const userData = db.prepare('SELECT * FROM users WHERE user_id = ?').get(req.user.user_id);
        if (!userData) return res.sendStatus(401);
        let userRes = genRealUser(userData);
        if (req.query.inventory) {
            let userDataInv = db.prepare('SELECT * FROM userInv WHERE user_id = ?').all(req.user.user_id);
            userDataInv = userDataInv.map(item => {
                const findItem = marketplace.find(item2 => {
                    return item2.filename == item.filename;
                })
                if (findItem) {
                    item.name = findItem.name
                }
                return item;
            })
            userRes.inventory = userDataInv;
        }
        return res.json(userRes)
    }).delete((req, res) => {
        if (!req.user) return res.sendStatus(401);
        db.prepare('DELETE FROM users WHERE user_id = ?').run(req.user.user_id);
        db.prepare('DELETE FROM userInv WHERE user_id = ?').run(req.user.user_id);
        //res.sendStatus(403);
        res.sendStatus(204);
    })
    eRouter.route("/:username").get((req, res) => {
        //if (!req.user) return res.sendStatus(401);
        const userData = db.prepare('SELECT * FROM users WHERE username = ?').get(req.params.username);
        if (!userData) return res.sendStatus(404);
        let userRes = genRealUser(userData);
        if (req.query.inventory) {
            const userDataInv = db.prepare('SELECT * FROM userInv WHERE user_id = ?').all(req.user.user_id);
            userRes.inventory = userDataInv;
        }
        return res.json(userRes)
    })
    return eRouter;
}
